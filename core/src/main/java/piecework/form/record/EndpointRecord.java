package piecework.form.record;

import piecework.form.model.Endpoint;

/**
 * @author James Renfro
 */
public class EndpointRecord implements Endpoint {

	private String id;
	private String url;
	private String method;
	private String mediaType;
	
	public EndpointRecord() {
		
	}
	
	public EndpointRecord(Endpoint contract) {
		this.id = contract.getId();
		this.url = contract.getUrl();
		this.method = contract.getMethod();
		this.mediaType = contract.getMediaType();
	}
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getMethod() {
		return method;
	}
	public void setMethod(String method) {
		this.method = method;
	}
	public String getMediaType() {
		return mediaType;
	}
	public void setMediaType(String mediaType) {
		this.mediaType = mediaType;
	}
	
}
